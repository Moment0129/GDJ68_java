package com.iu.study2;

public class Study2 {

	public static void main(String[] args) {
		// if, if-else, 
		// switch() case
		int num = 5;
		
		switch(num*2) {
		case 10 :
			System.out.println("num 10");
			break;
		default :
			System.out.println("num 모름");
			break;
		case 20 :
			System.out.println("num 20");
			break;
		}

	}

}
